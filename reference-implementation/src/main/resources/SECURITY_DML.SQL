CREATE TABLE SECURITY_USERS (
	USER_ID NUMBER NOT NULL,
	USERNAME VARCHAR2(32) NOT NULL,
	PASSWORD VARCHAR2(64) NOT NULL,
	BLOCKED  NUMBER(1) NOT NULL DEFAULT 0,
	CONSTRAINT SECURITY_USERS_PK PRIMARY KEY (USER_ID)
);

CREATE TABLE SECURITY_ROLES (
	ROLE_ID NUMBER NOT NULL,
	ROLENAME VARCHAR2(32) NOT NULL,
	CONSTRAINT SECURITY_ROLES_PK PRIMARY KEY (ROLE_ID)
);

CREATE TABLE SECURITY_USERS_IN_ROLES (
	USER_ID NUMBER NOT NULL,
	ROLE_ID NUMBER NOT NULL,
	CONSTRAINT SEC_USERS_IN_ROLES_USER_FK FOREIGN KEY(USER_ID) REFERENCES SECURITY_USERS(USER_ID),
	CONSTRAINT SEC_USERS_IN_ROLES_ROLE_FK FOREIGN KEY(ROLE_ID) REFERENCES SECURITY_ROLES(ROLE_ID)
);

CREATE SEQUENCE SECURITY_USERS_SEQ INCREMENT BY 1 START WITH 1 ORDER;
CREATE SEQUENCE SECURITY_ROLES_SEQ INCREMENT BY 1 START WITH 1 ORDER;
